print("Soma")
def soma(n1, n2):
    return(n1+n2)

print(soma(n1=45, n2=48))
print(soma(65, 68))

print("Subtração")
def subtracao(n1, n2):
    return(n1-n2)

print(subtracao(100, 50))
print(subtracao(26, 14))

print("Multiplicação")
def multiplicacao(n1, n2):
    return(n1*n2)
print(multiplicacao(20, 38))
print(multiplicacao(58, 32))

print("Divisão")
def divisao(n1, n2):
    return(n1/n2)
print(divisao(10, 5))
print(divisao(40, 2))
